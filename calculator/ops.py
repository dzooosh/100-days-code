""" 
    This contains the operation functions: add, substract, divide, multiply
"""

# add function
def add(num1, num2):
    """adds num1 and num2"""
    return num1 + num2

def subtract(num1, num2):
    """subtracts num2 from num1"""
    return num1 - num2

def divide(num1, num2):
    """divides num1 by num2"""
    return num1 / num2
def multiply(num1, num2):
    """multiplies num1 by num2"""
    return num1 * num2